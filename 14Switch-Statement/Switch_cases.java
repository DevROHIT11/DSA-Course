
public class Switch_cases {
    public static void main(String[]  args ){
        int num = 12;
        switch(num){

            case 1 : System.out.println("mango");
                break;

            case 2 : System.out.println("apple");
                break;

            case 3 : System.out.println("orange");
                break;

            default : System.out.println("halwa hai kya !!");
        }
        System.out.println();
    }
}
